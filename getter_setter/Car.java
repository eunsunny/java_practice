package getter_setter;

public class Car {
	private int speed;
	private boolean stop;
	
	// 메소드
	public int getSpeed() {
		return speed;
	}
	
	public void setSpeed(int speed) { //리턴받을 값이 없기떄문에 타입설정 안해도됨
		if (speed < 0) {
			this.speed = 0;
		} else {
			this.speed = speed;
		}
	}
	
	public boolean isStop() {
		return stop;
	}
	
	public void setStop(boolean stop) {
		this.stop = stop;
		this.speed = 0;
	}
}
